                    <h2>Proje Tabanlı Mevlâna Programı</h2>
                    <p>Proje Tabanlı Uluslararası Değişim Programı Yükseköğretim Kurulu Başkanlığı tarafından finanse edilen ve bilimsel çalışmaları destekleyen bir programdır. Bu program kapsamında iki kurum arasında hem değişim gerçekleştirilirken hem de proje çalışmaları sürdürülmektedir. Proje, İstanbul Üniversitesi-Cerrahpaşa ile Mevlâna kapsamında anlaşması olan üniversitelerle yürütülebilmektedir.  Proje kapsamında akademisyenlere günlük yevmiye ile ulaşım ücretleri ödenmektedir. Öğrenciler için ise 4 aylık burs desteği verilmektedir. Günlük yevmiye, hibeler ve ulaşım ücretleri başvurulan ülkeye göre değişmektedir. Ücretler için başvuru yapılan yılın YÖK tarafından ilan edilen Yürütme Kurul Kararı incelenmelidir. Yürütme Kurul Kararı’nda ayrıca ülkelere göre çalışma alanları (sosyal, sağlık, fen mühendislik) ve proje konuları da belirtilmiştir (web sayfamızda bulabilirsiniz).  Projede en az bir öğrencinin değişime katılması zorunludur. Araştırma görevlileri öğrenci statüsünde değerlendirileceklerdir. Akademisyenler en az 14, en fazla 89 gün değişim hareketliliğinde bulunabilirler. Proje kapsamında gelen/giden öğrenciler en az 1 dönem süresince değişimden yararlanabilirler. Değişim sürecinde izlenen yol Mevlâna Değişim Programı ile aynıdır.</p>
                    
